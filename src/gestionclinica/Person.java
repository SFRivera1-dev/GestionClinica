package gestionclinica;

/**
 *
 * @author sfriv
 */
public class Person {
    private String name, document, birthdate, adress, cellphone, email;

    public Person() {
    }

    public Person(String name, String document, String birthdate, String adress, String cellphone, String email) {
        this.name = name;
        this.document = document;
        this.birthdate = birthdate;
        this.adress = adress;
        this.cellphone = cellphone;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDocument() {
        return document;
    }

    public void setDocument(String document) {
        this.document = document;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    public String getCellphone() {
        return cellphone;
    }

    public void setCellphone(String cellphone) {
        this.cellphone = cellphone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Person{" + "name=" + name + ", document=" + document + ", birthdate=" + birthdate + ", adress=" + adress + ", cellphone=" + cellphone + ", email=" + email + '}';
    }
    
    
    
}
